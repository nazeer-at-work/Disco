#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
// Source of truth = the launcher drawables themselves. The in-app grid renders
// these same files via React Native's built-in `{ uri: '<drawable_name>' }`
// resource lookup on Android, so there is no separate (duplicated) gallery copy.
const sourceDir = path.join(root, 'android', 'app', 'src', 'main', 'res', 'drawable-nodpi');
const manualIconsPath = path.join(root, 'src', 'config', 'system-icons.json');
// Non-app drawables produced by generate-icon-pack that must never appear as icons.
const NON_ICON_DRAWABLES = new Set(['ic_fluffy_back', 'ic_fluffy_mask', 'ic_fluffy_upon']);
const generatedIconsPath = path.join(root, 'src', 'config', 'system-icons.generated.json');
const generatedImageMapPath = path.join(root, 'src', 'config', 'icon-image-map.generated.ts');

function toLabel(id) {
  const parts = id
    .replace(/[_-]+/g, ' ')
    .split(' ')
    .filter(Boolean)
    .map(part => {
      const lower = part.toLowerCase();
      if (lower === 'yt') {
        return 'YT';
      }
      if (lower.length <= 2) {
        return lower.toUpperCase();
      }
      return lower.charAt(0).toUpperCase() + lower.slice(1);
    });
  return parts.join(' ');
}

function stableHue(input) {
  let hash = 0;
  for (let i = 0; i < input.length; i += 1) {
    hash = (hash * 31 + input.charCodeAt(i)) % 4294967291;
  }

  const hue = hash % 360;
  const saturation = 58 + (hash % 9);
  const lightness = 62 + (hash % 8);
  return hslToHex(hue, saturation, lightness);
}

function hslToHex(h, s, l) {
  const sat = s / 100;
  const light = l / 100;
  const c = (1 - Math.abs(2 * light - 1)) * sat;
  const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
  const m = light - c / 2;

  let r = 0;
  let g = 0;
  let b = 0;

  if (h < 60) {
    r = c;
    g = x;
  } else if (h < 120) {
    r = x;
    g = c;
  } else if (h < 180) {
    g = c;
    b = x;
  } else if (h < 240) {
    g = x;
    b = c;
  } else if (h < 300) {
    r = x;
    b = c;
  } else {
    r = c;
    b = x;
  }

  const toHex = value => {
    const channel = Math.round((value + m) * 255)
      .toString(16)
      .padStart(2, '0');
    return channel;
  };

  return `#${toHex(r)}${toHex(g)}${toHex(b)}`.toUpperCase();
}

function main() {
  if (!fs.existsSync(sourceDir)) {
    throw new Error(`Icon source directory does not exist: ${sourceDir}`);
  }
  const manualIcons = JSON.parse(fs.readFileSync(manualIconsPath, 'utf8'));
  const manualById = new Map(manualIcons.map(icon => [icon.id, icon]));

  const iconFiles = fs
    .readdirSync(sourceDir)
    .filter(file => /^ic_fluffy_.+\.webp$/i.test(file))
    .sort((a, b) => a.localeCompare(b));

  // One entry per drawable. `drawable` is the Android resource name (no ext),
  // `id` is the dash-cased public id used by the icon list / labels.
  const discoveredById = new Map();
  iconFiles.forEach(file => {
    const drawable = path.basename(file, path.extname(file)); // e.g. ic_fluffy_google_meet
    if (NON_ICON_DRAWABLES.has(drawable) || /^ic_fluffy_symbol_/.test(drawable)) {
      return;
    }
    const id = drawable.replace(/^ic_fluffy_/, '').replace(/_/g, '-').toLowerCase();
    if (!discoveredById.has(id)) {
      discoveredById.set(id, { id, drawable });
    }
  });

  const discovered = Array.from(discoveredById.values())
    .sort((a, b) => a.id.localeCompare(b.id));

  const extras = discovered
    .filter(item => !manualById.has(item.id))
    .map(item => ({
      id: item.id,
      label: toLabel(item.id),
      hue: stableHue(item.id),
    }));

  const mergedIcons = [...manualIcons, ...extras];

  const generatedJson = `${JSON.stringify(mergedIcons, null, 2)}\n`;
  fs.writeFileSync(generatedIconsPath, generatedJson, 'utf8');

  const imageMapEntries = discovered
    .map(item => `  '${item.id}': { uri: '${item.drawable}' },`)
    .join('\n');

  const generatedTs = `import { ImageSourcePropType } from 'react-native';

// Auto-generated by scripts/generate-gallery-manifest.js — do not edit by hand.
// Each value is an Android drawable-resource URI; React Native resolves the bare
// name to res/drawable-nodpi/<name>.webp, so the grid reuses the launcher icons
// with zero duplicated image bytes.
export const iconImageMap: Record<string, ImageSourcePropType> = {
${imageMapEntries}
};
`;

  fs.writeFileSync(generatedImageMapPath, generatedTs, 'utf8');

  console.log(
    `[gallery-manifest] Wrote ${mergedIcons.length} icons (${extras.length} auto-discovered) and ${discovered.length} drawable URI mappings.`,
  );
}

main();
